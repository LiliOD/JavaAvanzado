var dia = prompt("Prompt the number of the day");

switch (dia) {
    case "1":
        alert("Hi, today is: Sunday");
        break;
    case "2":
        alert("Hi, today is: Monday");
        break;
    case "3":
        alert("Hi, today is: Tuesday");
        break;
    case "4":
        alert("Hi, today is: Wednesday");
        break;
    case "5":
        alert("Hi, today is: Thursday");
        break;
    case "6":
        alert("Hi, today is: Friday");
        break;
    case "7":
        alert("Hi, today is: Saturday");
        break;
    default:
        alert("That number does not correspond");
};